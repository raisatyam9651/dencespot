                    <h2 class="text-4xl font-display font-bold mb-8 italic">The Science Behind <span class="text-medicalTeal">Botox Injections</span></h2>
                    <div class="space-y-6 text-gray-600 leading-relaxed">
<p>Every time you smile, frown, or squint, your brain sends an electrical signal down the motor nerve, triggering the release of a neurotransmitter called acetylcholine. This chemical binds to the muscle receptors, causing the muscle to contract and fold the skin.</p>
<p>Botox works at the neuromuscular junction. It safely intercepts this process by temporarily preventing the release of acetylcholine. Without the chemical signal, the targeted muscle remains in a relaxed state. This physiological 'pause' stops the skin from wrinkling and prevents existing lines from deepening into permanent, static furrows.</p>
<p>The effects are localized purely to the treated muscle. The protein degrades naturally over 3 to 6 months, slowly restoring normal muscle activity. Because it acts only on motor nerves, it has absolutely no effect on your sensory nerves (your ability to feel touch, heat, or cold).</p>
