                <div class="text-gray-600 space-y-6 text-lg leading-relaxed text-left">
                    <p>A <strong>beard transplant</strong> is a minimally invasive cosmetic surgical procedure that permanently restores facial hair in areas affected by genetic patchiness, scarring, or hormonal imbalances. The procedure works by relocating healthy, DHT-resistant hair follicles from the back of your scalp (the occipital donor area) to the beard, moustache, jawline, or sideburn regions.</p>
                    
