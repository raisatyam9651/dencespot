                <p><strong>Vitamin B12:</strong> This is the most critical vitamin for hair colour. B12 deficiency is the number one nutritional cause of premature greying. It is found abundantly in eggs, dairy products, fish, and fortified cereals. If you follow a vegetarian diet, it is important to take a supplement as advised by your doctor.</p>

                <p><strong>Iron:</strong> Iron deficiency anaemia directly affects hair pigmentation. Spinach, beetroot, dates, and jaggery are all iron-rich foods that should be consumed daily. Taking them alongside Vitamin C enhances iron absorption.</p>

                <p><strong>Copper:</strong> Copper is directly required for melanin production. Almonds, cashews, dark chocolate, mushrooms, and sesame seeds are excellent sources of copper. Many people overlook the importance of copper, but it is a significant component of the <a href="which-vitamin-deficiency-causes-white-hair.php">vitamin and mineral deficiencies that cause white hair</a>.</p>

                <p><strong>Zinc:</strong> Zinc repairs hair follicles and helps keep melanocytes healthy. Pumpkin seeds, chickpeas, and whole grains are good sources of zinc.</p>

                <p><strong>Antioxidant-Rich Foods:</strong> Berries, green tea, dark leafy vegetables, and turmeric all combat the oxidative stress that contributes to premature greying. Aim to include a rainbow of colourful vegetables in your daily diet.</p>

                <h2>Lifestyle Changes — Small Steps That Make a Big Difference</h2>

                <h3>Stress Management — The Most Important Step</h3>

                <p>A 2020 Harvard study proved that chronic stress can permanently damage melanocyte stem cells. This means that once these cells are lost due to stress, they do not regenerate. This is why stress management is essential not only for mental health but also for the health of your hair.</p>

                <p><strong>What to do:</strong> Practise 15 to 20 minutes of meditation or deep breathing daily. Exercise regularly — it helps reduce cortisol (the stress hormone). Spend weekends on nature walks or pursuing hobbies. Make a conscious effort to control overthinking.</p>

