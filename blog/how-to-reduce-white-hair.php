                <h3>5. Black Seed Oil (Kalonji Oil) — The Hidden Gem</h3>

                <p>Kalonji oil contains thymoquinone, a powerful antioxidant that stimulates melanogenesis — the process of melanin production. Studies indicate that regular use can help restore pigmentation in grey hair, especially if the greying is recent.</p>

                <p><strong>How to use:</strong> Massage kalonji oil into the scalp before going to bed at night. Wash it off with a mild shampoo in the morning. Consuming one teaspoon of kalonji seeds daily is also beneficial.</p>

