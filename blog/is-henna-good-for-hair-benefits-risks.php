                <h2>Who Should Avoid Henna?</h2>
                <p>Henna is not for everyone. You should avoid or be cautious with henna if:</p>

                <p>You plan to get chemical hair coloring or perming in the near future. You have very dry or damaged hair that needs moisture rather than coating. You want a hair color other than reddish-brown tones. You have a known allergy to henna or Lawsonia inermis. You are using compound or "black" henna products.</p>

