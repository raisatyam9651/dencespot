                <h2>High-Impact Foods for Rapid Hair Fall Control</h2>

                <div class="my-12 rounded-3xl overflow-hidden shadow-lg">
                    <img src="../images/blog/emergency-hair-foods.png" alt="High impact foods for hair fall" class="w-full">
