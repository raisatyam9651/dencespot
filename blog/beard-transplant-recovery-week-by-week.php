                <h2>Critical Post-Op Care Rules</h2>

                <p>The decisions you make in the weeks following surgery have a direct, measurable impact on your graft survival rate. Follow these protocols strictly:</p>

                <ul>
                    <li><strong>No Direct Sun Exposure (First 4 Weeks):</strong> UV radiation inflames and damages newly settling follicles. Cover the face or stay indoors when sun exposure is unavoidable.</li>
                    <li><strong>No Gym, Swimming, or Strenuous Exercise (First 2 Weeks):</strong> Elevated blood pressure and sweat can dislodge unsettled grafts and introduce bacteria to open implantation sites.</li>
                    <li><strong>No Alcohol (First 10 Days):</strong> Alcohol thins the blood and significantly increases post-operative swelling, which delays healing.</li>
                    <li><strong>No Touching or Scratching (First 2 Weeks):</strong> Even gentle touching transfers bacteria and risks mechanically pulling out a graft that has not yet fully anchored.</li>
                    <li><strong>Elevate Head While Sleeping (First Week):</strong> Reduces swelling and prevents facial pressure on fresh implantation sites.</li>
                    <li><strong>Take Prescribed Medications:</strong> Complete the full antibiotic course and use the prescribed healing spray as directed. Do not self-substitute with over-the-counter alternatives.</li>
                </ul>

                <h2>Maximising Your Results During Recovery</h2>

                <p>Beyond the immediate protective protocols, there are active steps you can take to optimise graft survival and accelerate the growth timeline. Nutrition plays a significant role — a diet rich in protein, biotin, zinc, and iron directly fuels the keratin production your new follicles depend on. Avoid smoking entirely during the first 3 months, as nicotine dramatically constricts the blood vessels that supply your newly rooted follicles.</p>

