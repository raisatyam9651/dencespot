                <h2>The First 24 Hours: Day of Surgery to Night One</h2>

                <p>Immediately after the procedure, the transplanted area will appear red, slightly swollen, and covered in hundreds of tiny newly implanted hair stubs. The local anesthesia will begin wearing off 2-4 hours after the procedure ends, and mild tenderness or a dull aching sensation at the donor site (back of scalp) is normal. You will be sent home with your post-operative care kit, oral antibiotics, and detailed washing instructions.</p>

                <p>Sleep on your back with your head elevated on two pillows. Do not allow your face to press against the pillow surface, as the grafts are not yet anchored. Avoid touching, rubbing, or examining the transplanted area. The hair stubs will appear very upright and prominent — this is normal and expected.</p>

                <h2>Week 1 (Days 1–7): Crusting and Social Downtime</h2>

                <p>Within 24 to 48 hours, the micro-wounds around each implanted follicle form tiny scabs or crusts. These pinpoint crusts cover the entire transplanted zone and are a completely healthy biological response — nature's micro-bandages sealing each implantation site. The face will appear noticeably red and dotted with tiny dark specks.</p>

                <p>This week requires social downtime for most patients. The appearance is not alarming medically, but it is noticeable up close. Begin gentle saline washing as instructed from day 3 onwards. The objective is to keep the area clean and hydrated without physically disrupting the settling grafts. Pat dry only — never rub. Swelling, particularly under the eyes and on the upper cheeks, may peak around day 2-3 and then gradually reduce through the end of the week.</p>

                <h2>Week 2 (Days 8–14): Crusts Fall, Face Returns to Normal</h2>

                <p>By the beginning of week 2, the crusts will begin falling off naturally with gentle washing. Do not pick, scratch, or forcibly remove them — each crust contains the base of a follicle that has not yet fully rooted into the blood supply. By day 10-14, the vast majority of crusts have shed naturally, and the face begins returning to a visually normal, presentable state.</p>

                <p>The transplanted hairs themselves are still present, appearing as short, thin stubs. Many patients feel cautiously optimistic at this stage, seeing the distribution of new follicles across their previously bare areas. You can return to most normal social and professional activities by the end of week 2.</p>

                <h2>Weeks 3–4: The Shedding Phase (Do Not Panic)</h2>

                <p>Between weeks 2 and 4, the transplanted hair shafts begin falling out. This is the most psychologically challenging phase of the entire recovery and the point at which uninformed patients mistakenly believe the procedure has failed. <strong>It has not.</strong> This shedding is called the telogen phase, and it is a completely normal biological response to follicular relocation trauma.</p>

                <p>The hair shaft is shed, but the root — the living follicle — remains intact beneath the skin, dormant and establishing its new blood supply. Think of it as a tree losing its leaves in winter before aggressively flowering in spring. The follicles will emerge again, producing permanent, terminal hairs from month 3 onwards.</p>

