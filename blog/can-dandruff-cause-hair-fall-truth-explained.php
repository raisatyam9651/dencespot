                <p><strong>Selenium Sulfide 2.5%:</strong> Cell turnover slow karta hai. Oily dandruff mein effective hai.</p>
                <p><strong>Salicylic Acid:</strong> Scales ko dissolve karta hai. Thick, crusty dandruff mein kaam aata hai.</p>

