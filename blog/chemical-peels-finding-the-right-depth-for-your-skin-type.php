                peels, a fan is used to keep you comfortable. After a set amount of time, the acid is either neutralized
                or the skin is cooled down, and a soothing moisturizer and high-SPF sunscreen are applied.</p>

            <h2>Post-Peel Care: The Key to Success</h2>
            <p>The "peeling" part of a chemical peel is just as important as the acid application. For medium peels,
                your skin will feel tight and may turn brown or red before it starts to flake. You MUST resist the urge
                to pick or pull at the skin. Let it shed naturally. Forcefully peeling the skin can cause permanent
                scarring and hyperpigmentation.</p>

            <p>Additionally, your "new" skin will be extremely sensitive to UV rays. You must apply a broad-spectrum
