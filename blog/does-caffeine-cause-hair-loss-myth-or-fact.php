                <h3>Coffee Rinse</h3>
                <p>2 cups strong black coffee brew karein aur thanda hone dein. Shampoo ke baad is coffee se baal rinse karein. 5 minutes rakhein phir paani se wash karein. Week mein 2 baar karein. Yeh baalon ko shine bhi deta hai aur caffeine ka mild topical benefit bhi milta hai.</p>

                <h3>Coffee + Coconut Oil Mask</h3>
