                <p>A single hair strand can support up to 100 grams of weight. With 100,000 strands, your entire head of hair could theoretically support the weight of two elephants—though we do not recommend testing this.</p>

                <p>Hair is the second fastest growing tissue in the body, after bone marrow. Each strand grows approximately 0.3 to 0.5 millimeters per day, or about six inches per year.</p>

