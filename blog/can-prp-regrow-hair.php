                <h3>Stage 1: Follicle Reactivation (Weeks 1-4)</h3>
                <p>Growth factors stimulate dormant follicle stem cells. Blood supply to the scalp improves. You may not see visible changes yet, but cellular activity is increasing beneath the surface.</p>

                <h3>Stage 2: Reduced Shedding (Weeks 4-8)</h3>
                <p>Hair fall decreases noticeably. Follicles transition from the telogen (resting) phase to the anagen (growth) phase. Your pillow and shower drain show fewer fallen hairs. This is the first encouraging sign that PRP is working.</p>

                <h3>Stage 3: New Vellus Hair (Months 3-4)</h3>
                <p>Fine, light-coloured baby hairs begin to appear in thinning areas. These vellus hairs are newly activated follicles producing their first growth cycle. They will gradually thicken over time.</p>

                <h3>Stage 4: Visible Regrowth (Months 5-8)</h3>
                <p>Vellus hairs mature into terminal hairs — thicker, darker, and more visible. Hair density improves, and the scalp becomes less visible through the hair. This is when most patients feel genuinely excited about their progress.</p>

